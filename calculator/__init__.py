'''This is the implementaion for calculator'''
def add(a, b):
    '''add function for calculator'''
    return a + b

def substract(a, b):
    '''substraction function for calculator'''
    return a - b

def multiply(a, b):
    '''multiplication function for calculator'''
    return a * b

def divide(a, b):
    '''division function for calculator'''
    return a / b
